    //Elimina todo lo escrito en los campos de la interfaz    
    private void limpiarCajas () {
        cajaDestino.setText("");
        comboCapitanes.setSelectedIndex(-1);
        comboBarcos.setSelectedIndex(-1);
        cajaFecha.setText("");
        cajaHora.setText("");
    }
    
    //Activa o desactiva las cajas cuando se elimina
    private void activar (boolean estado) {
        cajaDestino.setEnabled(estado);
        comboBarcos.setEnabled(estado);
        comboCapitanes.setEnabled(estado);
        cajaFecha.setEnabled(estado);
        cajaHora.setEnabled(estado);
        textoActualizar.setVisible(estado);
    }
    
    //Cambia la visibilidad de algunas cajas
    private void visibilidad (boolean estado) {
        comboIdentificacionSalidas.setVisible(estado);
        textoIdentificacionEditar.setVisible(estado);
        botonBuscar.setVisible(estado);
        botonGuardar.setVisible(estado);
        comboIdentificacionSalidas.setEnabled(estado);
        estado = !estado;
        crear.setEnabled(estado);
        visualizar.setEnabled(estado);
        eliminar.setEnabled(estado);
        textoCrear.setVisible(estado);
        textoVisualizar.setVisible(estado);
        jLabel1.setVisible(estado);
        jLabel3.setVisible(estado);
        jLabel10.setVisible(estado);
    }
    
    //Metodo para limitar la cantidad de texto que se le ingresa
    private void limitarLetrasCajas (int limite, javax.swing.JTextField caja, java.awt.event.KeyEvent e) {
        if (caja.getText().length() >= limite) {
            getToolkit().beep();
            e.consume();
        }
    }
    
    //Carga las listas
    private void cargarLista () {
        comboCapitanes.removeAllItems();
        LinkedList lista = actividadBD.Navegantes();
        for (int i = 0; i < lista.size(); i++) {
            comboCapitanes.addItem(lista.get(i).toString());
        }
        comboCapitanes.setSelectedIndex(-1);
        comboBarcos.removeAllItems();
        PBarco barcoBD = new PBarco();
        lista = barcoBD.listaBarcos();
        for (int i = 0; i < lista.size(); i++) {
            comboBarcos.addItem(lista.get(i).toString());
        }
        comboBarcos.setSelectedIndex(-1);
        barcoBD.cerrarConexion();
    }
    
    //Carga las actividades
    private void cargarActividades () {
        comboIdentificacionSalidas.removeAllItems();
        LinkedList lista = actividadBD.Actividades();
        for (int i = 0; i < lista.size(); i++) {
            comboIdentificacionSalidas.addItem(lista.get(i).toString());
        }
        comboIdentificacionSalidas.setSelectedIndex(-1);
    }
    
