    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        if (comboIdentificacionSalidas.getSelectedIndex() != -1) {
            comboIdentificacionSalidas.setEnabled(false);
            if (botonGuardar.getText().equals("Borrar")) {
                activar(false); 
            }
            a = actividadBD.buscarSalida(comboIdentificacionSalidas.getSelectedItem().toString());
            comboCapitanes.setSelectedItem(a.getIdentificacionNavegante());
            comboBarcos.setSelectedItem(String.valueOf(a.getNumeroMatricula()));
            cajaFecha.setText(a.getFecha());
            cajaHora.setText(a.getHora());
            cajaDestino.setText(a.getDestino());
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        if (comboIdentificacionSalidas.getSelectedIndex() != -1) {
            if (botonGuardar.getText().equals("Borrar")) { //Borra o si no actualiza
                actividadBD.eliminar(comboIdentificacionSalidas.getSelectedItem().toString()); //elimina una actividad de la base de datos
            } else {
                if (comboIdentificacionSalidas.getSelectedIndex() != -1 && comboCapitanes.getSelectedIndex() != -1 && comboBarcos.getSelectedIndex() != -1 && !cajaFecha.getText().isEmpty() && !cajaHora.getText().isEmpty() && !cajaDestino.getText().isEmpty()) {
                    a = new Actividad(Integer.parseInt(comboIdentificacionSalidas.getSelectedItem().toString()), 
                            comboCapitanes.getSelectedItem().toString(), 
                            Integer.parseInt(comboBarcos.getSelectedItem().toString()), 
                            cajaFecha.getText(), cajaHora.getText(), 
                            cajaDestino.getText(), false);
                    actividadBD.actualizarActividad(a); //Actualizar con un objeto de actividad
                }
            }
        }
        textoEliminar.setText("Eliminar");
        activar(true);
        visibilidad(false);
        limpiarCajas();
    }//GEN-LAST:event_botonGuardarActionPerformed

