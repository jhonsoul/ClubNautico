    private void crearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_crearMouseClicked
        if (comboCapitanes.getSelectedIndex() != -1 && comboBarcos.getSelectedIndex() != -1 
                && !cajaFecha.getText().isEmpty() && !cajaHora.getText().isEmpty() 
                && !cajaDestino.getText().isEmpty()) { //Verifico que tenga infomación los textfield
            a = new Actividad(comboCapitanes.getSelectedItem().toString(), 
                    Integer.parseInt(comboBarcos.getSelectedItem().toString()), 
                    cajaFecha.getText(), cajaHora.getText(), cajaDestino.getText(), false); //Cargo el objeto de actividad con la información de los campos
            actividadBD.crearActividad(a);
            limpiarCajas();
        } else {
            JOptionPane.showMessageDialog(null, "Falto uno de los datos", "Error en operación", JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_crearMouseClicked

