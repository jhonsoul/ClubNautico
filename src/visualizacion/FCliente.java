    private void limpiarCajas () {
        cajaIdentificacion.setText("");
        cajaNombre.setText("");
        cajaApellido.setText("");
        cajaTelefono.setText("");
        cajaDireccion.setText("");
    }
    
    private void activar (boolean estado) {
        cajaNombre.setEnabled(estado);
        cajaApellido.setEnabled(estado);
        cajaTelefono.setEnabled(estado);
        cajaDireccion.setEnabled(estado);
    }
    
    private void visibilidad (boolean estado) {
        comboIdentificacion.setVisible(estado);
        textoIdentificacionEditar.setVisible(estado);
        botonBuscar.setVisible(estado);
        botonGuardar.setVisible(estado);
        comboIdentificacion.setEnabled(estado);
        estado = !estado;
        textoIdentificacionCrear.setVisible(estado);
        cajaIdentificacion.setVisible(estado);
        crear.setEnabled(estado);
        visualizar.setEnabled(estado);
        eliminar.setEnabled(estado);
        textoCrear.setVisible(estado);
        textoVisualizar.setVisible(estado);
        jLabel1.setVisible(estado);
        jLabel3.setVisible(estado);
        jLabel10.setVisible(estado);
    }
    
    private void limitarLetrasCajas (int limite, javax.swing.JTextField caja, java.awt.event.KeyEvent e) {
        if (caja.getText().length() >= limite) {
            getToolkit().beep();
            e.consume();
        }
    }
    
    private void cargarLista () {
        LinkedList lista = null;
        comboIdentificacion.removeAllItems();
        if (tipo.equals("socio")) {
            lista = socioBD.identificaciones();
        } else {
            lista = capitanBD.identificaciones();
        }
        for (int i = 0; i < lista.size(); i++) {
            comboIdentificacion.addItem(lista.get(i).toString());
        }
        comboIdentificacion.setSelectedIndex(-1);
    }
    
