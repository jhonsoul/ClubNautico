    private void crearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_crearMouseClicked
        //Utilizo para crear un nuevo socio o capitan dependiendo del texto que se encuentre en tipo
        if (tipo.equals("socio")) {
            if (!cajaIdentificacion.getText().isEmpty() && !cajaNombre.getText().isEmpty() && !cajaApellido.getText().isEmpty()) {
                s = new Socio(Integer.parseInt(cajaIdentificacion.getText()), 
                        cajaNombre.getText(), 
                        cajaApellido.getText(), 
                        cajaTelefono.getText(), 
                        cajaDireccion.getText(), false); //Cargo un socio para poder almacenarlo
                socioBD.crearSocio(s);
                limpiarCajas();
            } else {
                JOptionPane.showMessageDialog(null, "Falto uno de los datos Nombre, apellido o identificación del socio", "Error en operación", JOptionPane.ERROR_MESSAGE);
            } 
        } else {
            if (!cajaIdentificacion.getText().isEmpty() && !cajaNombre.getText().isEmpty() && !cajaApellido.getText().isEmpty()) {
                c = new Capitan(Integer.parseInt(cajaIdentificacion.getText()), 
                        cajaNombre.getText(), 
                        cajaApellido.getText(), 
                        cajaTelefono.getText(), 
                        cajaDireccion.getText(), false);//Cargo un capitan para poder almacenarlo(cajaDireccion cambia su objetivo para almacenar la licencia)
                capitanBD.crearCapitan(c);
                limpiarCajas();
            } else {
                JOptionPane.showMessageDialog(null, "Falto uno de los datos Nombre, apellido o identificación del capitan", "Error en operación", JOptionPane.ERROR_MESSAGE);
            } 
        }
    }//GEN-LAST:event_crearMouseClicked

