    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        if (comboIdentificacion.getSelectedIndex() != -1) {
            comboIdentificacion.setEnabled(false);
            if (botonGuardar.getText().equals("Borrar")) {
                activar(false); 
            }
            if (tipo.equals("socio")) {
                s = socioBD.cargarDatos(comboIdentificacion.getItemAt(comboIdentificacion.getSelectedIndex())); //Extraigo la infomacion de socio de la base de datos
                cajaNombre.setText(s.getNombre());
                cajaApellido.setText(s.getApellido());
                cajaTelefono.setText(s.getTelefono());
                cajaDireccion.setText(s.getDireccion());
            } else {
                c = capitanBD.cargarDatos(comboIdentificacion.getItemAt(comboIdentificacion.getSelectedIndex())); //Extraigo la infomacion de capitan de la base de datos
                cajaNombre.setText(c.getNombre());
                cajaApellido.setText(c.getApellido());
                cajaTelefono.setText(c.getTelefono());
                cajaDireccion.setText(c.getLicencia());
            }  
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        if (tipo.equals("socio")) {
            if (botonGuardar.getText().equals("Borrar")) {
                socioBD.eliminar(comboIdentificacion.getSelectedItem().toString());
            } else {
                if (!cajaNombre.getText().isEmpty() && !cajaApellido.getText().isEmpty()) {
                    s = new Socio(Integer.parseInt(comboIdentificacion.getSelectedItem().toString()), cajaNombre.getText(), cajaApellido.getText(), cajaTelefono.getText(), cajaDireccion.getText(), true);
                    socioBD.actualizarSocio(s);
                }
            }
        } else {
            if (botonGuardar.getText().equals("Borrar")) {
                capitanBD.eliminar(comboIdentificacion.getItemAt(comboIdentificacion.getSelectedIndex()));
            } else {
                if (!cajaNombre.getText().isEmpty() && !cajaApellido.getText().isEmpty()) {
                    c = new Capitan(Integer.parseInt(comboIdentificacion.getSelectedItem().toString()), cajaNombre.getText(), cajaApellido.getText(), cajaTelefono.getText(), cajaDireccion.getText(), true);
                    capitanBD.actualizarCapitan(c);
                }
            }
            
        }
        textoEliminar.setText("Eliminar");
        activar(true);
        visibilidad(false);
        limpiarCajas();
    }//GEN-LAST:event_botonGuardarActionPerformed

