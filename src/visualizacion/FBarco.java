    private void limpiarCajas () {
        cajaMatricula.setText("");
        cajaNombre.setText("");
        comboPropietario.setSelectedIndex(-1);
        comboAmarre.setSelectedIndex(-1);
    }
    
    private void cargarInterfazABarco () {
        String matricula = cajaMatricula.getText();
        String amarre = comboAmarre.getSelectedItem().toString();
        String propietario = comboPropietario.getSelectedItem().toString();
        String Nombre = cajaNombre.getText();
        b = new Barco(Integer.parseInt(matricula), amarre, propietario, Nombre, false);
    }
    
    private void activar (boolean estado) {
        cajaNombre.setEnabled(estado);
        textoActualizar.setVisible(estado);
    }
    
    private void visibilidad (boolean estado) {
        comboMatricula.setVisible(estado);
        textoIdentificacionEditar.setVisible(estado);
        botonBuscar.setVisible(estado);
        botonGuardar.setVisible(estado);
        comboMatricula.setEnabled(estado);
        estado = !estado;
        textoIdentificacionCrear.setVisible(estado);
        cajaMatricula.setVisible(estado);
        crear.setEnabled(estado);
        visualizar.setEnabled(estado);
        eliminar.setEnabled(estado);
        textoCrear.setVisible(estado);
        textoVisualizar.setVisible(estado);
        jLabel1.setVisible(estado);
        jLabel3.setVisible(estado);
        jLabel10.setVisible(estado);
    }
    
    //Limita la cantidad de texto que se le ingresa a la cajas de texto
    private void limitarLetrasCajas (int limite, javax.swing.JTextField caja, java.awt.event.KeyEvent e) {
        if (caja.getText().length() >= limite) {
            getToolkit().beep();
            e.consume();
        }
    }
    
    private void cargarListaBarcos () {
        LinkedList lista = barcoBD.listaBarcos();
        comboMatricula.removeAllItems();
        for (int i = 0; i < lista.size(); i++) {
            comboMatricula.addItem(lista.get(i).toString());
        }
        comboMatricula.setSelectedIndex(-1);
    }
    
    private void cargarListas () {
        PSocio idSocio = new PSocio();
        LinkedList lista = idSocio.identificaciones();
        comboPropietario.removeAllItems();
        comboAmarre.removeAllItems();
        for (int i = 0; i < lista.size(); i++) {
            comboPropietario.addItem(lista.get(i).toString());
        }
        comboPropietario.setSelectedIndex(-1);
        
        //Llenar lista de amarres solo los que este disponibles
        lista = barcoBD.listaAmarres();
        String numeroAmarre;
        for (int i = 0; i < 30; i++) {
            numeroAmarre = "A-" + i;
            for (int j = 0; j < lista.size(); j++) {
                if (!numeroAmarre.equals(lista.get(j))) { //Verifica disponibilidad
                    comboAmarre.addItem(numeroAmarre);
                }
            }
        }
        comboAmarre.setSelectedIndex(-1);
        idSocio.cerrarConexion();
    }
    
