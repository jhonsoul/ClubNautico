    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        if (comboMatricula.getSelectedIndex() != -1) {
            b = barcoBD.cargarDatos(comboMatricula.getItemAt(comboMatricula.getSelectedIndex()));
            comboMatricula.setEnabled(false);
            comboAmarre.setSelectedItem(b.getNumeroAmarre());
            comboPropietario.setSelectedItem(b.getIdentificacionDueño());
            cajaNombre.setText(b.getNombre());
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        if (comboMatricula.getSelectedIndex() != -1) {
            if (botonGuardar.getText().equals("Borrar")) {
                barcoBD.eliminar(comboMatricula.getSelectedItem().toString());
            } else {
                if (comboPropietario.getSelectedIndex() != -1 && comboAmarre.getSelectedIndex() != -1 && !cajaNombre.getText().isEmpty()) {
                    b.setNumeroMatricula(Integer.parseInt(comboMatricula.getSelectedItem().toString()));
                    b.setNumeroAmarre(comboAmarre.getSelectedItem().toString());
                    b.setIdentificacionDueño(comboPropietario.getSelectedItem().toString());
                    b.setNombre(cajaNombre.getText());
                    barcoBD.actualizarBarco(b);
                    limpiarCajas();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falto uno de los datos número de matricula, número de amarre, propietario o nombre del barco", "Error en operación", JOptionPane.ERROR_MESSAGE);
        }
        textoEliminar.setText("Eliminar");
        activar(true);
        visibilidad(false);
        limpiarCajas();
    }//GEN-LAST:event_botonGuardarActionPerformed

