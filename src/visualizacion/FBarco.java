    private void eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarMouseClicked
        if (textoEliminar.getText().equals("Regresar")) {
            visibilidad(false);
            textoEliminar.setText("Eliminar");
            textoActualizar.setText("Actualizar");
        } else {
            textoActualizar.setText("Eliminar");
            textoEliminar.setText("Regresar");
            visibilidad(true);
            cargarListaBarcos();
            textoEliminar.setVisible(true);
            cajaNombre.setEnabled(false);
            comboAmarre.setEnabled(false);
            botonGuardar.setText("Borrar");
        }
    }//GEN-LAST:event_eliminarMouseClicked

